    private void btoDivisaoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btoDivisaoMouseClicked
        // TODO add your handling code here:
        
        
        while(txt1.getText().isBlank()){
            txt1.setText(JOptionPane.showInputDialog("Digite um valor numerica"));
        }
        while (txt2.getText().isBlank()) {
           txt2.setText(JOptionPane.showInputDialog("Digite um valor numerica"));
            
        }
        double numero1 = Double.parseDouble(txt1.getText());
        double numero2 = Double.parseDouble(txt2.getText());
        if(numero2 == 0){
            txtbResultado.setText("Nao e possivel dividir por ZERO!");
        }
        
        txtbResultado.setText(Double.toString(Calcular.dividir(numero1, numero2)));
    }//GEN-LAST:event_btoDivisaoMouseClicked

